/**
 * Get a random quote from a specific concept
 * 
 * @param {string} concept - The philosophical concept
 * @returns {Object} - A quote object with text and author
 */
export const getRandomQuoteForConcept = (concept) => {
  const quotes = philosophicalQuotes[concept];
  
  if (!quotes || quotes.length === 0) {
    // Return a default quote if concept not found
    return {
      text: "The essence of philosophy is that we should live so that our happiness depends as little as possible on external causes.",
      author: "Epictetus"
    };
  }
  
  const randomIndex = Math.floor(Math.random() * quotes.length);
  return quotes[randomIndex];
};

/**
 * Get a random quote from any concept
 * 
 * @returns {Object} - A quote object with text and author
 */
export const getRandomQuote = () => {
  // Get all available concepts
  const concepts = Object.keys(philosophicalQuotes);
  
  // Select a random concept
  const randomConceptIndex = Math.floor(Math.random() * concepts.length);
  const randomConcept = concepts[randomConceptIndex];
  
  // Get a random quote from that concept
  return getRandomQuoteForConcept(randomConcept);
};

/**
 * Get a themed quote relevant to the CLI context
 * 
 * @returns {Object} - A quote object with text and author
 */
export const getCliThemedQuote = () => {
  // Concepts that are particularly relevant to the CLI theme
  const cliRelevantConcepts = [
    THEME.philosophicalConcepts.TECHNOLOGY,
    THEME.philosophicalConcepts.HUMAN_MACHINE,
    THEME.philosophicalConcepts.COMMAND_LINE,
    THEME.philosophicalConcepts.DIALOGUE
  ];
  
  // Select a random concept from the relevant ones
  const randomConceptIndex = Math.floor(Math.random() * cliRelevantConcepts.length);
  const randomConcept = cliRelevantConcepts[randomConceptIndex];
  
  // Get a random quote from that concept
  return getRandomQuoteForConcept(randomConcept);
};

